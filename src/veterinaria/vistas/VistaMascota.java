    private void cbMascotasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbMascotasActionPerformed
        Mascota mascota = (Mascota)cbMascotas.getSelectedItem();
        
        dcFecNac.setDate(Date.valueOf(mascota.getFecNac()));
        tfAlias.setText(mascota.getAlias());
        tfColorDePelo.setText(mascota.getColorDePelo());
        tfDuenio.setText(mascota.getDuenio().getApellidoNombre());
        tfEspecie.setText(mascota.getEspecie());
        tfId.setText(String.valueOf(mascota.getId()));
        tfPesoMedio.setText(String.valueOf(mascota.getPesoMedio()));
        tfRaza.setText(mascota.getRaza());
        tfSexo.setText(mascota.getSexo());
    }//GEN-LAST:event_cbMascotasActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        Mascota mascota = (Mascota)cbMascotas.getSelectedItem();
        
        mascota.setAlias(tfAlias.getText());
        mascota.setColorDePelo(tfColorDePelo.getText());
        
        mascota.setEspecie(tfEspecie.getText());
        mascota.setFecNac(dcFecNac.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate());
        mascota.setPesoMedio(Double.parseDouble(tfPesoMedio.getText()));
        mascota.setRaza(tfRaza.getText());
        mascota.setSexo(tfSexo.getText());
        mascotaData.actualizarMascota(mascota);
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        Mascota mascota = (Mascota) cbMascotas.getSelectedItem();
        mascotaData.borrarMascota(mascota.getId());
        
        cbMascotas.removeItemAt(cbMascotas.getSelectedIndex());
    }//GEN-LAST:event_btnBorrarActionPerformed


