    public VistaHistorialVisitas(Mascota mascota) {
        initComponents();
        
        try {
            this.mascota = mascota;
            modelo = new DefaultTableModel();
            conexion = new Conexion("jdbc:mysql://localhost/veterinaria", "root", "");
            visitaData = new VisitaData(conexion);
            visitas = (ArrayList)visitaData.obtenerVisitas();
            
            labelAlias.setText("Visitas de " + mascota.getAlias());
        
            armarCabecera();
            cargaDatos();
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(VistaHistorialVisitas.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }

