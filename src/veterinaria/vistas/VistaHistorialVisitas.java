    public void armarCabecera() {
        ArrayList<Object> columnas = new ArrayList<>();
        columnas.add("Fecha");
        columnas.add("Veterinario");
        columnas.add("Peso");
        columnas.add("Tratamiento");
        columnas.add("Detalles");
        for(Object item:columnas) {
            modelo.addColumn(item);
        }
        jtableVisitas.setModel(modelo);
    }
    
    public void cargaDatos(){
        for (Visita visita:visitas) {
            if(visita.getMascota().getId() == mascota.getId()) {
                modelo.addRow(new Object[]{visita.getFecha(), visita.getVeterinario(), visita.getPesoActual(), visita.getTratamiento().getDescripcion(), visita.getDetalles()});
            }
        }
    }
